    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (turno1 == false) {
            turno1 = true;
        } else {
            turno1 = false;
        }

        nFallos = 0;

        aciertos = jPasswordField1.getPassword().length;

        if (jPasswordField1.getText() == "") {
            jLabelError.setText("Inserte una palabra primero");
        }
        nLetras = jPasswordField1.getPassword().length;
        String guiones = "";
        for (int i = 0; i < nLetras; i++) {
            guiones = guiones + "- ";
        }
        this.guiones = new char[nLetras];
        jLabelGuiones.setText(guiones);
        for (int i = 0; i < nLetras; i++) {
            this.guiones[i] = '-';
        }
    }//GEN-LAST:event_jButton3ActionPerformed

