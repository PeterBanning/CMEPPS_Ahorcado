    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean ganado = false;
        boolean derrota = false;
        int marcador;
        //Miro la letra escrita la busco y actualizo los guiones
        if (ganado == false || derrota == false) {

            boolean esta = false;
            char[] contraseña = jPasswordField1.getPassword();

            for (int i = 0; i < contraseña.length; i++) {
                if (jTextFieldLetra.getText().toCharArray()[1] == contraseña[i]) {
                    aciertos--;
                    esta = true;
                    guiones[i] = jTextFieldLetra.getText().toCharArray()[1];
                }
            }

            if (esta) {
                String aux = "";
                for (int j = 0; j < guiones.length; j++) {
                    aux = aux + guiones[j];

                }
                System.out.println(aux);
                jLabelGuiones.setText(aux);
                jLabelError.setText("");

                if (aciertos == 0) {
                    ganado = true;
                }
            } else {
                nFallos++;
                jLabelFallos.setText(Integer.toString(nFallos));
                jLabelError.setText("La letra no pertenece a la palabra");

                if (nFallos > 5) {
                    derrota = true;
                }
            }
        }

        Imagen Imagen = new Imagen();
        jPanel1.add(Imagen);
        jPanel1.repaint();

        if (ganado) {
            if (turno1 == true) {
                jLabelError.setText("VICTORIA DEL JUGADOR 1");
                marcador = Integer.parseInt(jLabel11.getText());
                marcador++;
                jLabel11.setText(Integer.toString(marcador));
            } else {
                jLabelError.setText("VICTORIA DEL JUGADOR 2");
                marcador = Integer.parseInt(jLabel12.getText());
                marcador++;
                jLabel12.setText(Integer.toString(marcador));
            }
        } else if (derrota) {
            if (turno1 == true) {
                jLabelError.setText("VICTORIA DEL JUGADOR 2");
                marcador = Integer.parseInt(jLabel12.getText());
                marcador++;
                jLabel12.setText(Integer.toString(marcador));
            } else {
                jLabelError.setText("VICTORIA DEL JUGADOR 1");
                marcador = Integer.parseInt(jLabel11.getText());
                marcador++;
                jLabel11.setText(Integer.toString(marcador));
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

